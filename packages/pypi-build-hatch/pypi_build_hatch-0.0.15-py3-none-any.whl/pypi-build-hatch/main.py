"""Example module for demonstration."""

def add_one(number):
    """Add one to a number.

    Args:
        number: The number to add one to.

    Returns:
        The number plus one.
    """
    return number + 1 

def add_two(number):
    """Add two to a number.

    Args:
        number: The number to add two to.

    Returns:
        The number plus two.
    """
    return number + 2
